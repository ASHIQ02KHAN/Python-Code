# if statement
age = 18
if age >= 18:
    print("You are an adult")  # Runs if condition is True

# if-else
age = 16
if age >= 18:
    print("You are an adult")
else:
    print("You are underage")

# if-elif-else
marks = 85
if marks >= 90:
    print("Grade A")
elif marks >= 80:
    print("Grade B")
elif marks >= 70:
    print("Grade C")
else:
    print("Grade D")
