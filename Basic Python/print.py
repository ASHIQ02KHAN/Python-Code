# Basic print statement
print("Hello, World!")  # Prints text to the screen

# Printing multiple items
print("My name is", "Khan")  # Comma separates multiple values

# f-string formatting (new & preferred way)
name = "Khan"
print(f"My name is {name}")  # Inserts variable into string

# Using old format method
print("My name is {}".format(name))  # Works but f-string is simpler
