# Integer
age = 20  
print(type(age))  # Shows data type -> <class 'int'>

# Float
price = 99.99
print(type(price))  # <class 'float'>

# String
name = "Python"
print(type(name))  # <class 'str'>

# Boolean
is_student = True
print(type(is_student))  # <class 'bool'>

# Multiple variable assignment
x, y, z = 10, 20, 30
print(x, y, z)

# Assign same value to multiple variables
a = b = c = 5
print(a, b, c)
